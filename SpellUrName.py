def printVertically(name): # Print each letter below the previous one
    print()
    for letter in name: # Printing each letter completely then moving to the next one
        for i in alphaPix[letter]:
            for j in i:
                if j == 2:
                    print(letter,end='')
                if j == 0:
                    print(" ",end="")
            print()
        print()
        
def printHorizontally(name): # Print all letters next to each other
    print()
    listName = list(name)
    for rows in range(7): # Printing one row of each letter then moving to the next row
        for letter in listName:
            for i in alphaPix[letter][rows]:
                if i == 2:
                    print(letter,end='')
                if i == 0:
                    print(" ",end="")
            print(end="  ")
        print()
    print()
        
name = input("Enter your name: ")
# Removing space and lowering all letters
name = name.lower().replace(" ","")

HV = input("Do you want your name to be written Horizontally or Vertically? (H\\V) ")
if HV.lower() == "h":
    printHorizontally(name)
elif HV.lower() == "v":
    printVertically(name)
else:
    print("Invalid input! Only \"H\" or \"V\" are accepted")